# Técnica de Fatiamento

#tecnica 01
#[9] indoca que o programa mostrá o décimo cractere da frase (isso por que o programa começa a contar apartir do 0)
frase_1= 'O essencial é invisível aos olhos'
print(frase_1[13])

#técnica 02
#Aqui o progrma irá contar do 3º caractere ao 10º, mas , elerá excluir o 10º.

print(frase_1[9:21])

#técnica 03 
#Aqui ele fara o mesmo da tecnica 02, mas pulando de 2 em 2.
print(frase_1[9:21:2])

#técinca 04
#Aquí vai do zero ao cinco
print(frase_1[:5])

#técnica 05
#Aqui o programa fará o contrário da técnica 04
print(frase_1[5:])

#técnica 06
#Aqui o progrma começará a contar da 09 e irá pular de 3 em 3 até o final.
print(frase_1[9::3])

