n1 = float(input('Digite o primeiro número: \n'))
n2 = float(input('Digite o segundo número: \n'))

#Operador Aritimético
#O operando fica na frente do sinal ou depois do sinal
som = n1 + n2
sub = n1 - n2
pro = n1 * n2
div = n1 / n2
pot = n1 ** n2
dint = n1 // n2 # divisão inteira
rdiv = n1 % n2 # resto da divisão

print(f'A soma ente {n1} e {n2} é igual a {som}. ')
print(f'A subtração ente {n1} e {n2} é igual a {sub}. ')
print(f'O produto ente {n1} e {n2} é igual a {pro}. ')
print(f'A divisão ente {n1} e {n2} é igual a {div}. ')
print(f'A Potência ente {n1} e {n2} é igual a {pot}. ')
print(f'A Divisão Inteira ente {n1} e {n2} é igual a {dint}. ')
print(f'O resto da divisão ente {n1} e {n2} é igual a {rdiv}. ')


