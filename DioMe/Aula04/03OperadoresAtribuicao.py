saldo = 500
print(saldo)
saldo += 200 # adicina ao valor atribuido anteriormente à variável
print(saldo)

saldo /= 5 #divide por o valor atual da variável
print(saldo)

saldo //=5 # Mostra adivisaõ inteira do valor atual da variável
print(saldo)

saldo %= 5 #mostra o resto da divisão do valor atual dividido por 5.
print(saldo)

saldo **= 2 #Aqui elevei ao quadrado o valor atual(valor do resto da divisão)
print(saldo) # subtria um valor do saldo(Aqui é todo saldo)
saldo -= saldo
print(saldo)