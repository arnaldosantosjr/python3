#Tipo de dao bool (Boolean)
#Ao questionar algo em um programa só exitem duas respostas possíveis:
# sim(True) ou não(False)
#Exitem vários operadores para "questionar".
#Dentre os operadore o ==, que é um operador lógico que quetiona se o valro é igualo ao outro.

print(10 == 10) # sim => True (verdadeiro)
print(type(10 == 10)) 
print(10 == 11) #Não => False (falso)
print(type(10 == 11)) 
print(type(True)) 
