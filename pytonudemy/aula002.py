#Função print
'''A função print é usada para exibir as coisas na tela.
Elas são usadas par argumentos'''
#\r\n -> CRLF
#\r = return
#\n = linfint
print(34,18, ) #este tipo de argumento é o não nomeado
print(17,29, sep="-", end='\r\n') #Ete tipo de argumento é o nomedo (sep = separador, e podemos usar qualquer coisa para separar os elementos)
print(17,6, sep=",", end ='\n') #Ete tipo de argumento é o nomedo (sep = separador, e podemos usar qualquer coisa para separar os elementos)
